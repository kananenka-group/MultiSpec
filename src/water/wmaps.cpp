float WmapS::getcnn(const float Ei, const float Ej)
{
//
//  Intramolecular coupling of two hydroxyl stretches
// 
   float xi = 0.0; 
   float xj = 0.0;
   float pi = 0.0;
   float pj = 0.0;
   float wi = 0.0;
   float wj = 0.0;
   float wc = 0.0;

   if(intrac){
      if(ctype=="wsOH"){
         wi = getw01E_OH(Ei);
         wj = getw01E_OH(Ej);
         xi = getx01E_OH(wi);
         xj = getx01E_OH(wj);
         pi = getp01E_OH(wi);
         pj = getp01E_OH(wj);
      }else if(ctype=="wsOD"){
         wi = getw01E_OD(Ei);
         wj = getw01E_OD(Ej);
         xi = getx01E_OD(wi);
         xj = getx01E_OD(wj);
         pi = getp01E_OD(wi);
         pj = getp01E_OD(wj);
      }
      wc = (emap.wij0 + emap.wije*(Ei + Ej))*xi*xj + emap.wijpp*pi*pj;
   }
   return wc;
}

float WmapS::getcnn(const float ei, const float xi, const float pi,
                    const float ej, const float xj, const float pj)
{
   double wc;
   wc = (emap.wij0 + emap.wije*(ei + ej))*xi*xj + emap.wijpp*pi*pj;
   return wc;
} 
